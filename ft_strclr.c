#include "libft.h"

void	ft_strclr(char *s)
{
	int i;
	int len;

	i = 0;
	len = ft_strlen(s);
	if (!s)
	  return ;
	while (s[i] <= len)
	{
		ft_memset(s, '\0', len);
		i++;
	}
}
